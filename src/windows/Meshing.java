    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean error = false;
        double maxElemLengthLine = 0.15;
        double maxElemLengthUbend = 0.15;
        try {
            maxElemLengthLine = Double.parseDouble(maxLengthLineField.getText());
            maxElemLengthUbend = Double.parseDouble(maxLengthUBendField.getText());
        } catch (NumberFormatException e) {
            error = true;
        }

        jLabel1.setForeground(Color.black);
        if (!error) {
            Model.getInstance().mesh(maxElemLengthLine, maxElemLengthUbend);
            this.dispose();
        } else {
            jLabel1.setForeground(Color.red);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


