        double maxElemLength = 0.15;
        try {
            maxElemLength = Double.parseDouble(maxLengthField.getText());
        } catch (NumberFormatException e) {
            error = true;
        }
        
        jLabel1.setForeground(Color.black);
        if (!error) {
            Model.getInstance().mesh(maxElemLength);
            this.dispose();
        }else{
            jLabel1.setForeground(Color.red);
        }
